#!/usr/bin/env node
// json-to-clip-header.js
// Converts a SADIK JSON animation file into a C++ PROGMEM header for ESP32.
//
// Usage:
//   node json-to-clip-header.js <input.json> <output.h> [clip_name]
//
// If clip_name is omitted it is derived from the input filename stem.
// The output #defines and variable names are all prefixed with clip_name.
//
// Expected JSON structure:
//   {
//     "width":  128,      // optional, defaults to 128
//     "height":  64,      // optional, defaults to 64
//     "fps":     12,      // optional, defaults to 12
//     "frames": [         // required — array of frames
//       [b0, b1, …, b1023],   // each frame is an array of 1024 byte values
//       …
//     ]
//   }

'use strict';

const fs   = require('fs');
const path = require('path');

// ── CLI args ──────────────────────────────────────────────────────────────────

const [,, inputFile, outputFile, clipNameArg] = process.argv;

if (!inputFile || !outputFile) {
    console.error('Usage: node json-to-clip-header.js <input.json> <output.h> [clip_name]');
    process.exit(1);
}

// Derive clip name from filename stem if not provided.
const clipName  = clipNameArg || path.basename(inputFile, path.extname(inputFile));
const nameUpper = clipName.toUpperCase().replace(/-/g, '_');   // sanitise hyphens

// ── Parse JSON ────────────────────────────────────────────────────────────────

let json;
try {
    const raw = fs.readFileSync(inputFile, 'utf8');
    json = JSON.parse(raw);
} catch (e) {
    console.error(`[json-to-clip-header] Failed to read/parse "${inputFile}": ${e.message}`);
    process.exit(1);
}

const frames     = Array.isArray(json.frames) ? json.frames : [];
const fps        = (typeof json.fps    === 'number' && json.fps    > 0) ? json.fps    : 12;
const width      = (typeof json.width  === 'number' && json.width  > 0) ? json.width  : 128;
const height     = (typeof json.height === 'number' && json.height > 0) ? json.height : 64;
const frameBytes = Math.ceil((width * height) / 8);   // 1024 for 128×64

if (frames.length === 0) {
    console.warn(`[json-to-clip-header] Warning: "${inputFile}" has no frames — generating 1-frame placeholder.`);
}

// ── Build output ──────────────────────────────────────────────────────────────

const lines = [];

lines.push(`#pragma once`);
lines.push(`#include <Arduino.h>`);
lines.push(`// Auto-generated by scripts/json-to-clip-header.js — do not edit manually.`);
lines.push(`// Source: ${path.basename(inputFile)}`);
lines.push(``);
lines.push(`#define ${nameUpper}_FRAME_COUNT  ${frames.length || 1}`);
lines.push(`#define ${nameUpper}_FRAME_WIDTH  ${width}`);
lines.push(`#define ${nameUpper}_FRAME_HEIGHT ${height}`);
lines.push(`#define ${nameUpper}_FPS          ${fps}`);
lines.push(``);

// ── Per-frame byte arrays ─────────────────────────────────────────────────────

const actualFrames = frames.length > 0 ? frames : [[]];   // at least 1 placeholder

for (let fi = 0; fi < actualFrames.length; fi++) {
    const rawFrame = Array.isArray(actualFrames[fi]) ? actualFrames[fi] : [];

    // Pad or truncate to the exact frame size.
    const bytes = new Array(frameBytes);
    for (let bi = 0; bi < frameBytes; bi++) {
        const val = rawFrame[bi];
        bytes[bi] = (typeof val === 'number' && isFinite(val))
            ? (val & 0xFF)
            : 0;
    }

    // Split hex bytes into rows of 16 for readability.
    const hexRows = [];
    for (let row = 0; row < height; row++) {
        const rowStart  = row * (width / 8);
        const rowBytes  = bytes.slice(rowStart, rowStart + width / 8);
        hexRows.push('    ' + rowBytes.map(b => `0x${b.toString(16).padStart(2, '0')}`).join(', '));
    }

    lines.push(`static const uint8_t PROGMEM ${clipName}_frame_${fi}[${frameBytes}] = {`);
    lines.push(hexRows.join(',\n'));
    lines.push(`};`);
    lines.push(``);
}

// ── Pointer array ─────────────────────────────────────────────────────────────

lines.push(`static const uint8_t* const ${clipName}_frames[] PROGMEM = {`);
for (let fi = 0; fi < actualFrames.length; fi++) {
    const comma = fi < actualFrames.length - 1 ? ',' : '';
    lines.push(`    ${clipName}_frame_${fi}${comma}`);
}
lines.push(`};`);
lines.push(``);

// ── Write output ──────────────────────────────────────────────────────────────

try {
    fs.mkdirSync(path.dirname(outputFile), { recursive: true });
    fs.writeFileSync(outputFile, lines.join('\n'), 'utf8');
    console.log(`[json-to-clip-header] OK  ${outputFile}  (${actualFrames.length} frame(s), ${fps} FPS, ${width}×${height})`);
} catch (e) {
    console.error(`[json-to-clip-header] Failed to write "${outputFile}": ${e.message}`);
    process.exit(1);
}
